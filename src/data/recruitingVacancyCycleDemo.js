export function formatCycleSalary(n) {
  return `${String(n).replace(/\B(?=(\d{3})+(?!\d))/g, ' ')} сум`
}

export function formatCycleDate(iso) {
  const [y, m, d] = iso.split('-')
  return `${d}.${m}.${y}`
}
